// basic functions add, subtract, multiply, divide
function add(a, b) {
    return a + b;
};

function subtract(a, b) {
    return a - b;
};

function multiply(a, b) {
    return a * b;
};

function divide(a, b) {
    return a / b;
};

console.log(add(3, 4));
console.log(subtract(5, 2));
console.log(multiply(4, 5));
console.log(divide(6, 4));